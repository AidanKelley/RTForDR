/**
 * Converts HSV to RGB https://en.wikipedia.org/wiki/HSL_and_HSV#From_HSV
 * @param hsv array [h, s, v] 0 <= h < 360, 0 <= s < 1, 0 <= v < 1;
 */
function hsv2rgb(hsv) {
    var h = hsv[0],
        s = hsv[1],
        v = hsv[2],
        rgb = new Array(3),
        c = v * s,
        hPrime = h/60,
        x = c * (1 - Math.abs(hPrime % 2 - 1)),
        m = v - c;

    if(hPrime < 0 || hPrime === null || isNaN(hPrime) || hPrime === undefined) {
        console.log("Error: " + h);
        rgb = [0, 0, 0];
    }
    else if(hPrime < 1) {
        rgb = [c, x, 0];
    }
    else if(hPrime < 2) {
        rgb = [x, c, 0];
    }
    else if(hPrime < 3) {
        rgb = [0, c, x];
    }
    else if(hPrime < 4) {
        rgb = [0, x, c];
    }
    else if(hPrime < 5) {
        rgb = [x, 0, c];
    }
    else if(hPrime < 6) {
        rgb = [c, 0, x];
    }
    else {
        console.log(h + "->" + hPrime);
        rgb = [0, 0, 0];
    }

    for(var i = 0; i < 3; i++) {
        rgb[i] += m;
    }

    return rgb;
}
/**
 *
 * @param dose [0, 1)
 */
function getColorForDose(dose) {
    var max = 0,
        min = 240,
        saturation = 1,
        value = 1;
    if(dose > 0) {
        return hsv2rgb([(dose) * (max - min) + min, saturation, value]);
    }
    else {
        // console.log("Returning 0...");
        return [0, 0, 0];
    }
}
